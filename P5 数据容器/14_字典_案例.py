employee = {
    '王': {'部门': "科技部", '工资': 3000, '级别': 1},
    '周': {'部门': "市场部", '工资': 5000, '级别': 2},
    '林': {'部门': "市场部", '工资': 7000, '级别': 3},
    '张': {'部门': "科技部", '工资': 4000, '级别': 1},
    '刘': {'部门': "市场部", '工资': 6000, '级别': 2}
}

print(employee)

# 将级别为1的员工，级别+1，工资+1000
for key in employee:
    if employee[key]['级别'] == 1:
        employee[key]['级别'] += 1
        employee[key]['工资'] += 1000

print(employee)