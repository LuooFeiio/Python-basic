# \ + enter 可以使句内换行，避免一句话看起来太长
print('这是一句话', \
      '后面接了一句话', \
      '又接了一句话')

print("Hello \
World. \
Nice to come.") # Hello World. Nice to come.

message = 'Hello World!'
print(message, \
      'Nice to come.')

