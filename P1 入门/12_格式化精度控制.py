# 使用辅助符号“m.n”控制数据的宽度和精度
# m：宽度，若宽度小于数字自身则不生效（很少用）
# .n：小数部分精度，小数会四舍五入

height = 1.77
year = 2026
name = "雒飞"
m2 = "我是%s，出生于%8d年，身高是%.2f米" % (name, year, height) # [空][空][空][空]2008; 1.77
print(m2)

num2 = 11.345
print("数字11.345的宽度设置为7，小数精度设置为2：%7.2f" % num2)
print("数字11.345的小数精度设置为3：%.3f" % num2)